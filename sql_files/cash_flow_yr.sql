    total_cash_from_operating_activities NUMERIC,
    issuance_of_capital_stock NUMERIC,
    depreciation NUMERIC,
    other_cashflows_from_investing_activities NUMERIC,
    dividends_paid NUMERIC,
    change_to_inventory NUMERIC,
    change_to_account_receivables NUMERIC,
    sale_purchase_of_stock NUMERIC,
    other_cashflows_from_financing_activities NUMERIC,
    change_to_netincome NUMERIC,
