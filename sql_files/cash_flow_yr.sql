    change_receivables NUMERIC,
    cash_flows_other_operating NUMERIC,
    exchange_rate_changes NUMERIC,
    cash_and_cash_equivalents_changes NUMERIC,
    change_in_working_capital NUMERIC,
    stock_based_compensation NUMERIC,
    other_non_cash_items NUMERIC,
    free_cash_flow NUMERIC,
    created_date DATE NOT NULL, 
	last_update_date DATE NOT NULL
